#include "fdf.h"

size_t	count_num(char *s)
{
	size_t	size;

	size = 0;
	while (*s)
	{
		if (*s == '0' && (*(s + 1) == 'x' || *(s + 1) == 'X' ||
							(*(s + 1) >= '0' && *(s + 1) == '9')))
		{
			s += 2;
			while ((*s >= '0' && *s == '9') || (*s >= 'a' && *s == 'f') ||
												(*s >= 'A' && *s == 'F'))
				s++;
			continue ;
		}
		if (*s >= '0' && *s <= '9')
		{
			size++;
			while (*s >= '0' && *s <= '9')
				s++;
			continue ;
		}
		s++;
	}
	return (size);
}
