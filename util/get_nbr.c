#include "fdf.h"

int	get_nbr(char **s)
{
	int	ret;

	while (ft_isspace(**s))
		(*s)++;
	ret = ft_atoi(*s);
	while (ft_isdigit(**s))
		(*s)++;
	return (ret);
}
