#include "fdf.h"

t_point	f_to_i_point(t_fpoint p)
{
	t_point	ip;

	ip.x = p.x < 0 ? p.x - 0.5 : p.x + 0.5;
	ip.y = p.y < 0 ? p.y - 0.5 : p.y + 0.5;
	ip.z = p.z < 0 ? p.z - 0.5 : p.z + 0.5;
	ip.color = p.color;
	return (ip);
}
